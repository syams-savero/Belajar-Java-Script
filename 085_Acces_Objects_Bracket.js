// Cara mengakses Objects menggunakan bracket
// Fungsinya adalah jika nama Properties dalam Objects menggunakan spasi
// Contoh :
let makanan = {
    "makanan pembuka": "Sop",
    "makanan inti": "Ayam",
    "makanan penutup": "Kue"
}

// Cara menggunakan bracket untuk memanggil isi dari Objects
console.log(makanan["makanan inti"]);       // Ayam
console.log(makanan["makanan penutup"]);    // Kue
console.log(makanan["makanan pembuka"]);    // Sop
