// Cara singkat untuk menambahkan nilai pada variabel
// Cara sebelumnya 
var a = 5;
var b = "Cihuy";
// jadikan var a menjadi angka 7 dan var b menjadi "Aku Cihuy"
a = a + 2;
b = b + "Aku ";
// dengan begini var a menjadi 7 dan var b menjadi "Aku Cihuy"
// tapi itu cara lama, cara cepatnya adalah
a += 2;
b += "Aku ";
// kedua cara ini sama saja hasilnya, hanya beda cara penulisan